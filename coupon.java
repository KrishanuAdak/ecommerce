package MODEL;

public class coupon {
	public int id;
	public String name;
	public int coupon_off;
	public String coupon_code;
	public String description;
	public coupon(int id, String name, int off, String coupon_code, String description) {
		super();
		this.id = id;
		this.name = name;
		this.coupon_off = off;
		this.coupon_code = coupon_code;
		this.description = description;
	}
	
	

}
